// front end developer show
var displays = 1;

function fed() {

    // front end developer page
    var fed1 = document.getElementById('fed-open');
    
    if (displays == 1) {
        fed1.style.display = "block";
        displays = 1;
    }
}

var displays2 = 1;
function fedclose() {

    // front end developer close page
    var fed2 = document.getElementById('fed-open');
    
    if (displays == 1) {
        fed2.style.display = "none";
        displays2 = 1;
    }
}

